                <img class="img-fluid" src="assets/img/relacionabejaplanta.jpg" width="300px" height="300px">
                <p>
                    Las plantas cuentan con células reproductivas a las cuales se les conoce como polen, donde al momento de realizar la polinización las abejas transportan dicho polen de una flor a otra
                    lo cual permite que las plantas que florecen puedan producir semilla o fruta facilitando su reproducción.
