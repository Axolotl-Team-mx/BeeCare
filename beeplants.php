                <h1 class="text-center">¿Cómo realiza la polinización las abejas?</h1>
                <img class="img-fluid" src="assets/img/abejapolinizando.jpg" width="500px" height="700px">
                <p>
                Cuando pensamos en una abeja, una de las cosas más llamativas es que sus cuerpos tienen “pelos”, los cuales en realidad se les conoce como setas, las cuales son diferentes según la parte del cuerpo de la abeja.

                En el caso de las especies de abejas más selectivas, las setas presentan formas especializadas que facilitan la recolección y manipulación del polen. Las setas con formas muy especializadas se pueden ubicar en lugares muy específicos del cuerpo de la abeja.

                Por otro lado, abejas que recolectan polen de las plantas tropicales tienen setas engrosadas alrededor de la boca. Las abejas que se especializan en plantas con granos de polen muy grandes, poseen setas muy separadas entre sí, lo cual permite que los granos de polen se incrusten y sean transportados por la abeja.
                </p> <br>

                <p>
                <h1 class="text-center">Tipos de polinización</h1>
                Incidental: el objetivo principal de forrajeo es el néctar (también aceites o resinas) y el polen se acumula en el cuerpo de la abeja de manera pasiva, ya sea por la consistencia pegajosa del polen, sus ornamentaciones o por electrostática, mientras la abeja se mueve por la flor en busca de néctar. El polen se pega de manera indistinta en el cuerpo de la abeja y puede descartarlo cuando se limpia o lo puede trasladar a las áreas especializadas del cuerpo (escopa o corbícula) para ser transportado al nido.

                Activa: el objetivo principal del forrajeo es el polen. Dentro de esta categoría se han descrito seis formas en las que las abejas obtienen el polen:<br>
                <ul>
                    <li>Raspar con las extremidades: las abejas usan las patas o las partes bucales para raspar el polen directamente de las anteras.</li><br>
                    <li>Vibrar: las abejas usan contracciones de los músculos del tórax para producir vibraciones que son transmitidas del cuerpo de la abeja a las anteras, de manera que el polen es expulsado, este comportamiento también es conocido como polinización por vibración y se da en flores donde el polen está guardado dentro de las anteras.</li><br>
                    <li>Frotar con el cuerpo o las escopas: las abejas frotan continuamente las anteras con su tórax y abdomen o con la zona de las patas donde se recolecta el polen (escopa), el movimiento realizado es adelante-atrás, arriba-abajo sobre la flor. </li><br>
                    <li>Golpeteo: las abejas recolectan el polen usando golpecitos rápidos con el abdomen, el movimiento es arriba-abajo.</li><br>
                    <li>Frotar con la cara: las abejas frotan las anteras continuamente con la cara. Donde las abejas usan los “pelos” para recolectar el polen. </li><br>
                    <li>Raspar: este comportamiento solo ha sido observado en abejas que recolectan polen de flores del género Penstemon (familia Plantaginaceae; Campanitas); las anteras están parcialmente guardadas dentro de la corola de la flor. Para ello, las abejas frotan las anteras con la parte dorsal del tórax, moviendo todo el cuerpo dentro y fuera de la flor, esta fricción genera vibraciones que liberan el polen de las anteras.</li><br>
                </ul>
                </p>

